public class Zak {
    private String jmenoAPrijmeni;
    private int rocnik;


    public Zak(String jmenoAPrijmeni, int rocnik) {
        this.jmenoAPrijmeni = jmenoAPrijmeni;
        this.rocnik = rocnik;
    }

    public String getJmenoAPrijmeni() {
        return jmenoAPrijmeni;
    }

    public void setJmenoAPrijmeni(String jmenoAPrijmeni) {
        this.jmenoAPrijmeni = jmenoAPrijmeni;
    }

    public int getRocnik() {
        return rocnik;
    }

    public void setRocnik(int rocnik) {
        this.rocnik = rocnik;
    }
}
